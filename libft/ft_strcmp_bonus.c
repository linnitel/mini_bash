#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	size_t	i;

	i = 0;
	if (!s1 && s2 != NULL)
		return ((unsigned char)s2[i]);
	if (!s2 && s1 != NULL)
		return ((unsigned char)s1[i]);
	if (!s1 && !s2)
		return (0);
	while (s1[i] == s2[i] && (s1[i] != '\0' || s2[i] != '\0'))
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
